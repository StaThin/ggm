# Traduzione italiana dei principali termini tecnici utilizzati nella teoria dei grafi

## Elementi Fondamentali

- Vertex / Node: Vertice / Nodo.
- Edge: Spigolo / Lato (nei grafi non orientati) o Arco (nei grafi orientati).
- Directed Graph (Digraph): Grafo orientato (o Digrafo).
- Undirected Graph: Grafo non orientato.

## Strutture e Proprietà

- Path: Cammino (sequenza di nodi in cui l'ordine conta).
- Cycle: Ciclo (un cammino chiuso).
- Loop: Cappio (un arco che parte e finisce nello stesso nodo).
- Degree: Grado di un nodo (numero di collegamenti).
- In-degree / Out-degree: Grado di ingresso / Grado di uscita (nei grafi orientati).
- Connected Component: Componente connessa.

## Rappresentazioni in Codice

- Adjacency Matrix: Matrice di adiacenza.
- Adjacency List: Lista di adiacenza.

## Operazioni e Algoritmi

- Traversal / Search: 
- Visita / Esplorazione del grafo.
- Breadth-First Search (BFS): Visita in ampiezza.
- Depth-First Search (DFS): Visita in profondità.
- Topological Sort: Ordinamento topologico.